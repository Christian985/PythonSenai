print("Selecione um dia da semana em números entre 1 e 12")
mes_selecionado = int(input(""))
if mes_selecionado == 1:
    print("Janeiro")
elif mes_selecionado == 2:
    print("Fevereiro")
elif mes_selecionado == 3:
    print("Março")
elif mes_selecionado == 4:
    print("Abril")
elif mes_selecionado == 5:
    print("Maio")
elif mes_selecionado == 6:
    print("Junho")
elif mes_selecionado == 7:
    print("Julho")
elif mes_selecionado == 8:
    print("Agosto")
elif mes_selecionado == 9:
    print("Setembro")
elif mes_selecionado == 10:
    print("Outubro")
elif mes_selecionado == 11:
    print("Novembro")
elif mes_selecionado == 12:
    print("Dezembro")
else:
    print("Erro")
