print("Selecione um dia da semana em números entre 1 e 7")
dia_selecionado = int(input(""))
if dia_selecionado == 1:
    print("Segunda")
elif dia_selecionado == 2:
    print("Terça")
elif dia_selecionado == 3:
    print("Quarta")
elif dia_selecionado == 4:
    print("Quinta")
elif dia_selecionado == 5:
    print("Sexta")
elif dia_selecionado == 6:
    print("Sábado")
elif dia_selecionado == 7:
    print("Domingo")
else:
    print("Erro")
